package calculator;

public class Calculator {

	public static int sum(int a, int b) {
		int sum = a + b;
		return sum;
	}

	public static int product(int a, int b) {
		int product = a * b;
		return product;
	}

	public static int subtract(int a, int b) {
		int subtract = a - b;
		return subtract;
	}

	public static double divide(double a, double b) {
		double divide = a - b;
		return divide;
	}

}
