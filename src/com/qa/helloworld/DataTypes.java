package com.qa.helloworld;

public class DataTypes {

	public static void main(String[] args) {
		byte byteOne = 22;
		short shortOne = 79;
		int intOne = 999;
		long longOne = 1000000000l;

		float floatOne = 12.44f;
		double doubleOne = 99.99;

		boolean booleanOne = true;

		char charOne = 's';

		System.out.println(byteOne + shortOne + intOne + longOne + floatOne + doubleOne + charOne);
		System.out.println(booleanOne);
	}

}
